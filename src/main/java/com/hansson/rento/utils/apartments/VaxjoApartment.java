package com.hansson.rento.utils.apartments;

public class VaxjoApartment {
	private String Street;
	private int RentPerMonthSort;
	private double ObjectAreaSort;
	private String ObjectSubDescription;
	private String PlaceName;
	private String SeekAreaDescription;
	private String ObjectNo;

	public String getStreet() {
		return Street;
	}

	public void setStreet(String street) {
		Street = street;
	}

	public int getRentPerMonthSort() {
		return RentPerMonthSort;
	}

	public void setRentPerMonthSort(int rentPerMonthSort) {
		RentPerMonthSort = rentPerMonthSort;
	}

	public double getObjectAreaSort() {
		return ObjectAreaSort;
	}

	public void setObjectAreaSort(double objectAreaSort) {
		ObjectAreaSort = objectAreaSort;
	}

	public String getObjectSubDescription() {
		return ObjectSubDescription;
	}

	public void setObjectSubDescription(String objectSubDescription) {
		ObjectSubDescription = objectSubDescription;
	}

	public String getPlaceName() {
		return PlaceName;
	}

	public void setPlaceName(String placeName) {
		PlaceName = placeName;
	}

	public String getSeekAreaDescription() {
		return SeekAreaDescription;
	}

	public void setSeekAreaDescription(String seekAreaDescription) {
		SeekAreaDescription = seekAreaDescription;
	}

	public String getObjectNo() {
		return ObjectNo;
	}

	public void setObjectNo(String objectNo) {
		ObjectNo = objectNo;
	}

}
